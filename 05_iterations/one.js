// for loop
for(let index = 0; index < 10; index++){
    const element = index;
    console.log(element);
}


// The loop starts with index set to 0.
// It continues to run as long as index is less than 10.
// In each iteration, index is incremented by 1.
// The current value of index is assigned to element and logged to the console.
// The output will be the numbers 0 to 9 each on a new line.

